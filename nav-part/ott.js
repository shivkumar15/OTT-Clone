let navANDlogo_hover=document.querySelector(".navANDlogo");
navANDlogo_hover.addEventListener("mouseover",function(){
    navANDlogo_hover.classList.add("navANDlogo-hover");
})

navANDlogo_hover.addEventListener("mouseleave",function(){
    navANDlogo_hover.classList.remove("navANDlogo-hover");
})